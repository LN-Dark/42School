int	ft_str_is_uppercase(char *str)
{
	int	p;

	p = 0;
	if (str[p] == '\0')
	{
		return (1);
	}
	while (str[p] != '\0')
	{
		if (!(str[p] >= 'A' && str[p] <= 'Z'))
		{
			return (0);
		}
		p++;
	}
	return (1);
}
