#include "libft.h"

int	ft_strncmp(const char *s1, char *s2, size_t n)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i] && i < n)
	{
		i++;
	}
	if (s1[i] == s2[i])
		return (0);
	return (s1[i] - s2[i]);
}
