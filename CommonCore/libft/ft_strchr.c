#include "libft.h"

char	*ft_strchr(const char *a, int i)
{
	char *s;
	int	j;

	j = 0;
	s = (char *)a;
	while (s[j] != '\0')
	{
		if (s[j] == i)
			return (s + j);
		j++;
	}
	if (i == '\0')
		return (s);
	return (0);
}
